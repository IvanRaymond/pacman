package de.amr.games.pacman.model.world.arcade;

import de.amr.games.pacman.model.world.api.Food;

/**
 * Food found in Arcade version of the game.
 * 
 * @author Armin Reichert
 */
public enum ArcadeFood implements Food {

	PELLET, ENERGIZER;

	@Override
	public int fat() {
		return this == PELLET ? 1 : 3;
	}
}